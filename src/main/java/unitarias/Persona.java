package unitarias;

public class Persona {
    private String cedula, nombre, apellido;
    private int edad;
    private float estatura;

    public Persona() {
    }

    public Persona(String cedula, String nombre, String apellido, int edad, float estatura) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
        this.estatura = estatura;
    }
    
    
    
}
